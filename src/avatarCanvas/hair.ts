export function hairTemplateFactory(type: HairType): FeatureTemplate {
    switch(type) {
        case HairTypes.SPIKEY:
            return spikey;
        case HairTypes.EMO:
            return emo;
        case HairTypes.FLAT:
            return flat;
        case HairTypes.CENTERPART:
            return centerpart;
        case HairTypes.BOB:
            return bob;
        case HairTypes.BEARD:
            return beard;
        case HairTypes.MESSY:
            return messy;
        case HairTypes.PONY:
            return pony;
        case HairTypes.NONE:
        default:
            return none;
    }
}