export function hairTemplateFactory(type: HairType): FeatureTemplate {
    switch(type) {
        case HairTypes.SPIKEY:
            return spikey;
        case HairTypes.EMO:
            return emo;
        case HairTypes.FLAT:
            return flat;
        case HairTypes.CENTERPART:
            return centerpart;
        case HairTypes.BOB:
            return bob;
        case HairTypes.BEARD:
            return beard;
        case HairTypes.MESSY:
            return messy;
        case HairTypes.PONY:
            return pony;
        case HairTypes.HORNS:
            return horns;
        case HairTypes.COWBOY:
            return cowboy;
        case HairTypes.FLAME:
            return flame;
        case HairTypes.WIZARD:
            return wizard;
        case HairTypes.PRINCESS:
            return princess;
        case HairTypes.HALO:
            return halo;
        case HairTypes.CAP:
            return cap;
        case HairTypes.ANTENNA:
            return antenna;
        case HairTypes.SIGIL:
            return sigil;
        case HairTypes.SUNGLASSES:
            return sunglasses;
        case HairTypes.KNOBS:
            return knobs;
        case HairTypes.NONE:
        default:
            return none;
    }
}